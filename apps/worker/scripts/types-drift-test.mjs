// MH-MAINT-001 drift gate: the committed worker-configuration.d.ts must match
// what `wrangler types` generates from the current wrangler.toml. Changing a
// binding without regenerating types fails this check.
import { spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const WORKER_DIR = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const COMMITTED = path.join(WORKER_DIR, "worker-configuration.d.ts");

// Banner lines carry a config hash + tool versions; version-only noise is
// tolerated, every substantive difference is not.
function normalize(text) {
  return text
    .split("\n")
    .filter(
      (line) =>
        !line.startsWith("// Generated by Wrangler") &&
        !line.startsWith("// Runtime types generated with"),
    )
    .join("\n")
    .trim();
}

// Generate INSIDE the worker dir: the emitted `typeof import("./src/index")`
// is relative to the output file, so a /tmp path would always differ.
const generated = path.join(WORKER_DIR, `.types-drift-${process.pid}.d.ts`);
// process.exitCode (not process.exit) so the finally cleanup always runs.
try {
  const result = spawnSync(
    "pnpm",
    ["exec", "wrangler", "types", "--path", generated],
    { cwd: WORKER_DIR, encoding: "utf8", timeout: 120_000 },
  );
  if (result.status !== 0) {
    console.error(result.stdout || "");
    console.error(result.stderr || "");
    console.error("types-drift: `wrangler types` failed");
    process.exitCode = 1;
  } else {
    const expected = normalize(fs.readFileSync(generated, "utf8"));
    const actual = normalize(fs.readFileSync(COMMITTED, "utf8"));
    if (expected !== actual) {
      console.error(
        "types-drift: worker-configuration.d.ts is stale.\n" +
          "Run `pnpm --filter @markhub/worker exec wrangler types` and commit the result.",
      );
      process.exitCode = 1;
    } else {
      console.log("types-drift: worker-configuration.d.ts matches wrangler.toml");
    }
  }
} finally {
  fs.rmSync(generated, { force: true });
}
